public class Test6 {
    public static void main(String[] args) {
        char a = 'A'; // 向 char 类型的 a 变量赋值为 A，所对应的 ASCII 值为 65
        char b = 'B'; // 向 char 类型的 b 变量赋值为 B，所对应的 ASCII 值为 66
        System.out.println("A 的 ASCII 值与 B 的 ASCII 值相加结果为：" + (a + b));
        System.out.println("A 的 ASCII 值与 B 的 ASCII 值相加结果为：" + a + b);
    }
}
